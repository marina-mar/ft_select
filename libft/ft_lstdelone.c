#include "includes/libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	if (!alst || !del)
		return ;
	del((*alst)->content, (size_t)((*alst)->content_size));
	ft_memdel((void**)alst);
}
